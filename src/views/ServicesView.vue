          const el = document.querySelector(hash)
          if (el) {
            el.scrollIntoView({ behavior: 'smooth', block: 'center' })
          }
        })
      }
    }
  },
  mounted() {
    // Handle hash navigation for service anchors on initial load
    this.scrollToHash(this.$route.hash)
  },
  watch: {
    // Watch for hash changes when navigating within the same route
    '$route.hash'(newHash) {
      this.scrollToHash(newHash)
    }
  }
}
